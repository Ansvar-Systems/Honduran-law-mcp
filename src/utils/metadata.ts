/**
 * Response metadata utilities for Honduran Law MCP.
 */

import type Database from '@ansvar/mcp-sqlite';

export interface ResponseMetadata {
  data_source: string;
  jurisdiction: string;
  disclaimer: string;
  freshness?: string;
  note?: string;
  query_strategy?: string;
}

export interface ToolResponse<T> {
  results: T;
  _metadata: ResponseMetadata;
}

export function generateResponseMetadata(
  db: InstanceType<typeof Database>,
): ResponseMetadata {
  let freshness: string | undefined;
  try {
    const row = db.prepare(
      "SELECT value FROM db_metadata WHERE key = 'built_at'"
    ).get() as { value: string } | undefined;
    if (row) freshness = row.value;
  } catch {
    // Ignore
  }

  return {
    data_source: 'Honduran Law (Congreso Nacional de Honduras) — official legislative portal',
    jurisdiction: 'HN',
    disclaimer:
      'This data is sourced from publicly available Honduran legislation. ' +
      'Always verify with the official Congreso Nacional de Honduras portal.',
    freshness,
  };
}
